package com.uwl.service.domain;


public class WeatherX {

	private String region;
	private String date;
	private String to;
	public WeatherX(String region, String date, String to) {
		super();
		this.region = region;
		this.date = date;
		this.to = to;
	}
	public WeatherX() {
	}
	/**
	 * @return the region
	 */
	public String getRegion() {
		return region;
	}
	/**
	 * @param region the region to set
	 */
	public void setRegion(String region) {
		this.region = region;
	}
	/**
	 * @return the date
	 */
	public String getDate() {
		return date;
	}
	/**
	 * @param date the date to set
	 */
	public void setDate(String date) {
		this.date = date;
	}
	/**
	 * @return the to
	 */
	public String getTo() {
		return to;
	}
	/**
	 * @param to the to to set
	 */
	public void setTo(String to) {
		this.to = to;
	}
	
}